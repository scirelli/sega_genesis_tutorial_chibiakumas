InfLoop:
	move.l #backgroundtilemapbase,a3	;Scroll the background a unit

	clr.l d0
	move.b (backgroundtilemapscrollx),d0
	addq.b #2,d0
	ifnd allowhalftilescroll
		addq.b #2,d0
	endif
	move.b d0,(backgroundtilemapscrollx)

	move.b (backgroundtilemapscrolly),d0
	addq.b #2,d0

	ifnd allowhalftilescroll
		addq.b #2,d0
	endif

	move.b d0,(backgroundtilemapscrolly)
	ifd allowhalftilescroll
		jsr setscroll		;full scroll redraw
	else
		jsr resetscroll		;partial scroll redraw
	endif

	jsr drawscreen			;Flush caches to screen

	
;Delay loop
	ifd BuildNeo
		move.l #$10000,d0
slowdown1:
		subq.l #1,d0
		bne slowdown1
	endif
	
	jmp infloop

	
