ReadJoystick:	;Returns %---FRLDU
	move.b (Joystickdata+1),d0	;Process Joy 1  ;	F---RLDU  	?
	roxl.b #1,d0			;Fire -> eXtend flag	---RLDU-   	F 
	rol.b #3,d0				;skip Unused bits		RLDU----   	F 
	roxr.b #1,d0			;Get back F				FRLDU---   	- 
	ror.b #3,d0				;Move needed bits back	---FRLDU   	- 
	eor.b #$FF,d0			;Flip the bits of the bottom byte
	rts

		
		
		
InstallJoysytickHandler:	;Install Joystick handler

	move.w #$14,-(sp)	;IKBD command $14 - set joystick event reporting
	move.w #4,-(sp)		;Device no 4 (keyboard - Joystick is part of kb)
	move.w #3,-(sp)		;Bconout (send data to the keyboard)
	trap #13			;BIOS Trap
	addq.l #6,sp		;Clean up the stack

	move.w #34,-(sp)	;return IKBD vector table (KBDVBASE)
	trap #14			;X-BIOS trap
	addq.l #2,sp 		;Clean up the stack
	
	move.l d0,(IkbdVector) 	;store the IKBD vectors address for later
	move.l d0,a0  			;A0 points to IKBD vectors
	move.l (24,a0),(OldJoystickVector)		;Backup old joystick vector
	
	move.l #JoystickHandler,(24,a0) ;Install our Joystick handler 
	rts
	
	
;This is our Joystick handler, Once installed it will be executed
; automatically by the firmware 

JoystickHandler:	
	move.b  (1,a0),(Joystickdata+0) ; store joy 0 data
	move.b  (2,a0),(Joystickdata+1)	; store joy 1 data
	rts  

IkbdVector:			dc.l 0 		; original IKBD vector storage
OldJoystickVector:	dc.l 0    	; original joy vector storage
Joystickdata:		ds.b 2		;Joypad bits F---RLDU 



