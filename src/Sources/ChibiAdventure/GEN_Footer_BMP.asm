

NewLine:
	clr.l d2			;Xpos =0
	addq.l #1,d5		;Ypos ++

	cmp.b #$17,d5		;Over bottom of the screen?
	bcc docls
	rts
	
docls:	
	ifnd cls 
Cls:
		moveM.l d1/d4/d3/d6,-(sp)
			clr.l d2		;Xpos
			clr.l d5		;Ypos
			
			move.b #$20,d3	;Char (Space)
			clr.b d6		;CharInc (0= all same character)
			
			move.b #$20,d1	;Width
			move.b #$18,d4	;Height
			jsr fillarea
		moveM.l (sp)+,d1/d4/d3/d6
		rts
	else
		jmp cls
	endif 
		
Slowdown:		;this exists for systems that run too fast
	asl #4,d0
	jmp pausea

	
ReadJoystick:		;D0=%S321RLDU
	movem.l d1-d2,-(sp)
		move.b #%01000000,($A10009)	; Set direction IOIIIIII (I=In O=Out)
		move.l #$A10003,a0		;RW port for player 1
		
		move.b #$40,(a0)	; TH = 1
		nop					;Delay
		nop
		move.b (a0),d2		; d0.b = --CBRLDU	Store in D2
		
		clr.b (a0)			; TH = 0
		nop					;Delay
		nop
		move.b (a0),d1		; d1.b = --SA--DU	Store in D1
		
		move.b d2,d0
		and.l #%00001111,d0	;RLDU
		rol.b #1,d2
		and.b #%01100000,d2	;BC
		or.b d2,d0
				
		move.b d1,d2
		and.b #%00010000,d2	;A
		or.b d2,d0
		
		rol.b #2,d1
		and.b #%10000000,d1	;S
		or.b d1,d0
	movem.l (sp)+,d1-d2
	rts
	
	ifnd prepareVram
prepareVram:	;To select a memory location  we need to calculate 
				; the command byte... depending on the memory location
				; $7FFF0003 = Vram $FFFF... $40000000=Vram $0000
		
		
			move.l a6,d0
			and.w #%1100000000000000,d0	;Shift the top two bits to far right 
			rol.w #2,d0
			
			move.l a6,d6
			and.l #%0011111111111111,d6	;shift the other bits left two bytes
			rol.l #8,d6	
			rol.l #8,d6
			
			or.l d0,d6						
			or.l #$40000000,d6	;Set the second bit from the top to 1
								;#%01000000 00000000 00000000 00000000
			move.l d6,(VDP_ctrl) ;$C00004 VDP control
		
		rts
	endif 
	
