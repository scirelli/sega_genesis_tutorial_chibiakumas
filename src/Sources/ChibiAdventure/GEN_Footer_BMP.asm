		and.l #255,d2			;Xpos
		and.l #255,d5			;Ypos
			
		move.l d5,d7
		asl.l #5,d7				;Ypos *32
		
		add.l d2,d7				;Xpos *1
		
		asl.l #5,d7				;Tilenum*32
		
		add.l #256*32,d7		;+Tile 256 (First tile of BMP screen)
		
		move.l d7,a6
		movem.l d0/d6,-(sp)
			jsr prepareVram
		movem.l (sp)+,d0/d6

		ifd NativeSpr_Multiplatform2Bitplane

			movem.l d0/d1-d4,-(sp)
			move.b #8,d4
