			asl.l #2,d0
			roxl.b #1,d2
			roxl.l #1,d0
			roxl.b #1,d1
			roxl.l #1,d0
			
			dbra d3,PrintCharByteX
			
			move.l d0,(VDP_data)	;$C00000	Send the tile data to the VDP
			
			
			
			
			subq.b #1,d4
			bne PrintCharByteY

			movem.l (sp)+,d0/d1-d4
			


		else 
			move.l #15,d7			;Write the bitmap data into pattern VRAM
PrintCharSendBitmap:	
			move.w (a4)+,(VDP_data)	;$C00000 VDP Data port
			dbra d7,PrintCharSendBitmap
		endif 
		
