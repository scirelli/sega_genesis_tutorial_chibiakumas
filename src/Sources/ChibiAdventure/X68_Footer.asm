

NewLine:
	clr.l d2			;Xpos =0
	addq.l #1,d5		;Ypos ++

	cmp.b #$17,d5		;Over bottom of the screen?
	bcc docls
	rts

docls:	

	ifnd cls 
Cls:
		moveM.l d1/d4/d3/d6,-(sp)
			clr.l d2		;Xpos
			clr.l d5		;Ypos
			
			move.b #$20,d3	;Char (Space)
			clr.b d6		;CharInc (0= all same character)
			
			move.b #$20,d1	;Width
			move.b #$18,d4	;Height
			jsr fillarea
		moveM.l (sp)+,d1/d4/d3/d6
		rts
	endif
	
Slowdown:		;this exists for systems that run too fast
	asl #4,d0
	jmp pausea
	
	
	

ReadJoystick:		;Returns: %--21RLDU

	clr.b $E9A005			;8255 Port C  ($00=Normal Buttons
	move.b $E9A001,d7		;-21-RLDU	   $30= Extra buttons)
	
	move.b d7,d0
	and.l #%00001111,d0		;Keep Directions
	
	lsr.b #1,d7
	and.b #%00110000,d7		;Move fires
	or.b d7,d0
	or.b #%11000000,d0		;Fill unused bits
	rts	
	
	ifnd PrintChar
PrintNumber:
		add.b #48,d0		;ascii 0

PrintChar:				;print char a with sprite routine
		cmp.b #0,d0
		beq printchar0
		
		move.l d0,-(sp)
			sub.b #32,d0			;Font doesn't have chars below 32
			
			cmp.b #64,d0			;Lowercase?
			bcs notlowercase
				sub.b #32,d0		; convert to upper!
notlowercase:
			and.l #$FF,d0
			rol.l #5,d0				; 32 bytes per character
			move.l #Font,a4
			add.l d0,a4
					
			move.b d2,d7			;Convert X pos to Byte
			and.l #$FF,d7
			asl.l #4,d7				;2 bytes per pixel - 8 pixels per char
					
			;bclr.l #0,d7			;Clear Bit 0
			
			move.l #$c00000,a0		;Screen position in memory
			add.l d7,a0
			
			clr.l d7
			move.b d5,d7
			asl.l #3,d7				;Multiply Y by 8
			
			asl.l #8,d7				;1024 bytes per line
			asl.l #2,d7
			add.l d7,a0
					
			moveM.l d5/d6,-(sp)
				move.l #8-1,d5			;Lines
PrintCharY:
				move.l #4-1,d6			;Source bytes per line 
				
	;Our font has two pixels per byte, Screen has one pixel per WORD
PrintCharX:		
				move.b (a4),d7			;Get A byte
				lsr.b #4,d7
				move.w d7,(a0)+			;Write top nibble to VRAM
				move.b (a4)+,d7
				move.w d7,(a0)+			;Write bottom nibble to VRAM
				dbra d6,PrintCharX
				add.l #1024-16,a0		;Move down a line
				dbra d5,PrintCharY
			
			moveM.l (sp)+,d5/d6
		move.l (sp)+,d0

printchar0:
		addq.b #1,d2
		rts
	endif 
	

