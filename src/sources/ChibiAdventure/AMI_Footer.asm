Player_ReadControlsOne:	;Translate HV data into joystick values
		clr.l d0		;Will hold bit 8
		clr.l d1		;Will hold bit 9
		clr.l d3		;Will hold bit 0
		clr.l d4		;Will hold bit 1
		
;Get the 4 bits that are needed for the directions
		roxr.l #1,d2	;bit 0
		roxl.l #1,d3
		
		roxr.l #1,d2	;bit 1
		roxl.l #1,d4
		
		roxr.l #7,d2	;bit 8
		roxl.l #1,d0
		
		and.l #1,d2		;bit 9
		move.b d2,d1
		
;Calculate the new directions
		eor.b d0,d1		;Up (Bit 9 Xor 8)
		roxr.b d1
		roxr.b d0	
		
		move.b d4,d1	;Down (Bit 1 Xor 0)
		eor.b d3,d1
		roxr.b d1
		roxr.b d0		
		
		roxr.b d2		;Left (Bit 9)
		roxr.b d0		
		
		roxr.b d4		;Right (Bit 1)
		roxr.b d0		
		
		roxl.b d7		;Fire
		roxr.b d0		
		
		ror.b #3,d0
		eor.b #%11101111,d0	;Invert UDLR bits
		or.l #$FFFFFF00,d0	;Set unused bits
