

NewLine:
	clr.l d2			;Xpos =0
	addq.l #1,d5		;Ypos ++

	cmp.b #$17,d5		;Over bottom of the screen?
	bcc cls
	rts

Cls:
	moveM.l d1/d4/d3/d6,-(sp)
		clr.l d2		;Xpos
		clr.l d5		;Ypos
		
		move.b #$20,d3	;Char (Space)
		clr.b d6		;CharInc (0= all same character)
		
		move.b #$20,d1	;Width
		move.b #$18,d4	;Height
		jsr fillarea
	moveM.l (sp)+,d1/d4/d3/d6
	rts

	
Slowdown:		;this exists for systems that run too fast
	asl #4,d0
	jmp pausea

	
ReadJoystick:		;D0=%S321RLDU
	movem.l d1-d2,-(sp)
		move.b #%01000000,($A10009)	; Set direction IOIIIIII (I=In O=Out)
		move.l #$A10003,a0		;RW port for player 1
		
		move.b #$40,(a0)	; TH = 1
		nop					;Delay
		nop
		move.b (a0),d2		; d0.b = --CBRLDU	Store in D2
		
		clr.b (a0)			; TH = 0
		nop					;Delay
		nop
		move.b (a0),d1		; d1.b = --SA--DU	Store in D1
		
		move.b d2,d0
		and.l #%00001111,d0	;RLDU
		rol.b #1,d2
		and.b #%01100000,d2	;CB
		or.b d2,d0
				
		move.b d1,d2
		and.b #%00010000,d2	;A
		or.b d2,d0
		
		rol.b #2,d1
		and.b #%10000000,d1	;S
		or.b d1,d0
	movem.l (sp)+,d1-d2
	rts
	
		
PrintNumber:
	add.b #48,d0		;ascii 0
	
PrintChar:				;print char D0 with sprite routine
	cmp.b #0,d0
	beq printchar0
	move.l #$40000003,A0	;top 4=write, bottom $3=Cxxx range
	clr.l d7				;Tilemap at $C000+
	move.b d5,D7			;Ypos
	ifd BackgroundTilemapBase
		add.l #2,d7
	endif 
	rol.L #8,D7				;move $-FFF to $-FFF----
	rol.L #8,D7				;move $-FFF to $-FFF----
	rol.L #7,D7				;2 bytes per tile * 64 tiles per line
	add.L D7,A0				;add $4------3 (4=write 3=$C000 range)
	
	move.b D2,D7			;Xpos
	add #4,d7				;Center 256x192 screen
	rol.L #8,D7				;move $-FFF to $-FFF----
	rol.L #8,D7				;move $-FFF to $-FFF----
	rol.L #1,D7				;2 bytes per tile
	add.L D7,A0				;add $4------3 (4=write 3=$C000 range)
	move.l A0,(VDP_ctrl)	;$C00004 write next character to VDP

	move.l d0,d7			;Char num
	and.l #$FF,d7
	sub.b #32,d7			;no char below 32
	cmp.b #64,d7
	bcs notlowercase
	sub.b #32,d7			;we don't define lowercase, convert to upper!
notlowercase:
	ifd patterndata		;For Maxtile
		add.l #patterndata,d7
	endif 
	move.w d7,(VDP_data)	;$C00000 store next word of name data
printchar0:
	addq.l #1,d2
	rts

	
